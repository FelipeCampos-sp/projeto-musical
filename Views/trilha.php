         <h2 align="center">Os 10 games com as melhores e mais impactantes trilhas sonoras</h2>
         <section class="conteudo-trilha">
             <p>
                   Com o passar dos anos, a área de trilha sonora dos jogos vem ganhando 
                   um gigantesco destaque graças à criatividade de diversos artistas que inovam com o uso de suas obras, dando cada vez mais vida aos seus projetos. 
                   Tendo isso mente, criamos esta lista com os 10 games com as melhores trilhas sonoras criadas na história da indústria. Visto que, esta lista é
                   baseada nas trilhas que receberam grande atenção publica, não sendo uma lista opinativa definitiva.
             </p>
             
             <article class="banner">
                 <img class="img-fluid" "max-width: 100%; height: auto;" src="<?= BASE_URL?>/assets/img/bg-21.png">
                 <h1 style="font-weight:bold;">-The Legend of Zelda Breath of the Wild</h1>
               
                 <h2> Compositores: Manaka Kataoka, Yasuaki Iwata e Hajime Wakai</h2>
                  
                  <p>
                     Lançado em 2017, a trilha de Breath of the Wild , composta por veteranos da franquia, 
                     tiveram em mente o objetivo de criar algo diferente visto nos outros jogos. Com isso em mente, 
                     eles criaram uma trilha principalmente composta à base de piano, com melodias focadas em enaltecer a música e sons ambientes no jogo, 
                     trazendo assim um sentimento mais autentico ao cenário de mundo aberto apresentado no jogo.
                 </p>
             </article>
             
              <article class="banner">
                 <img class="img-fluid" "max-width: 100%; height: auto;" src="<?= BASE_URL?>/assets/img/bg-13.png">
                 <h1 style="font-weight:bold;">-Final Fantasy</h1>
                 
                 <h2>Compositor: Nobuo Uematsu</h2>             
                   
                 <p>
                        Lançado em 1997, a trilha de Final Fantasy VII continua, depois de 20 anos, sendo um grande marco na indústria dos jogos, 
                        com trilhas orquestrais escritas com o roteiro da história em mente, 
                        Nobuo Uematsu foi capaz de criar trilhas icônicas que se alastravam na época a 3 CDs de jogo, que até hoje são reproduzidas 
                        em concertos e referenciadas em jogos posteriores da franquia.
                 </p>
             </article>
             
          <article class="banner">
                 <img class="img-fluid" "max-width: 100%; height: auto;" src="<?= BASE_URL?>/assets/img/bg-15.png">
                 <h1 style="font-weight:bold;">-Journey</h1>
                 <h2>Compositor Austin Wintory</h2>
                    <p>
                        Lançado em 2012, a trilha de Journey, que levou três anos para ser composta, trouxe um arranjo musical 
                        composto em sua grande parte de tons fortes de instrumentos de cordas que trazem muito bem o sentimento de solidão 
                        que é passado no jogo, com melodias que se alteram em conjunto a jornada do jogador durante a história.
                 </p>
             </article>
             
               <article class="banner">
                 <img class="img-fluid" "max-width: 100%; height: auto;" src="<?= BASE_URL?>/assets/img/bg-16.png">
                    
                 <h1 style="font-weight:bold;">-Red Dead Redemption</h1>  
                 <h2>Compositores: Bill Elm e Woody Jackson</h2>
                    
                 <p>
                      Lançado em 2010, Red Dead Redemption possui uma trilha sonora completamente inspirada nos famosos filmes spaghetti western, 
                      colocando os jogadores em diversos momentos em eventos que, com a música, os fazem vivenciar uma experiência cinematográfica do velho oeste. 
                      Alem disso, umas das trilhas do jogo, “Far Away”, com  posta por Jose Gonzalez, é uma das músicas mais marcante do jogo, 
                      carregando todo o peso emocional da história do jogo e a passando direto para o jogador.
                 </p>
             
                          
              <article class="banner">
                 
                  <img class="img-fluid" "max-width: 100%; height: auto;" src="<?= BASE_URL?>/assets/img/bg-18.png">
                 
                 <h1 style="font-weight:bold;">-Silent Hill 2</h1>  
                 <h2>Compositor : Akira Yamaoka</h2>
                    
                 <p>
                     Lançado em 2001, Silent Hill 2 é até hoje considerado um dos grandes Survival Horror já criados, com suas músicas de rock significativas, 
                     trazendo sentimentos relacionados ao luto de perda e nostalgia, como também o seu forte uso de sons ambientes, como gritos, passos, 
                     vidros quebrados e máquinas industriais , tudo incorporado com intuito de trazer desconforto aos seus jogadores.
                 </p>
             </article>
             
             
             <article class="banner">
                 <img class="img-fluid" "max-width: 100%; height: auto;" src="<?= BASE_URL?>/assets/img/bg-17.png">
                 <h1 style="font-weight:bold;">-Shadow of the Colossus</h1>
                 <h2>Compositor: Kow Otani</h2>
           
                    <p>
                     Lançado em 2005, Shadow of the Colossus possui uma trilha distinta presente durante os combates do jogo, 
                     trazendo trilhas orquestrais que não só trazem a grandiosidade das lutas, como também detalham sutilmente 
                     a ambiguidade de suas ações dentro da história. Fora isso, a completa ausência de qualquer música de fundo durante 
                     as partes de mundo aberta, reforça ainda mais o sentimento de abandono ao jogador, com apenas os sons ambientes dentro 
                     da vastidão do cenário.


                 </p>
             </article>
             
            
             
              <article class="banner">
                 <img class="img-fluid" "max-width: 100%; height: auto;" src="<?= BASE_URL?>/assets/img/bg-19.png">
                 <h1 style="font-weight:bold;">-The Elder Scrolls V: Skyrim</h1>
                 <h2>Compositor: Jeremy Soule</h2>
                    <p>
                       Lançado em 2011, Skyrim possui uma trilha orquestral que permite o jogador passar por diversas experiências, de uma música coloquial relaxadora 
                       a uma mais intensa reservada para momentos marcantes de ação, a gigantesca variedade de tons somente demonstra a vastidão de opções 
                       que as pessoas têm experienciar  no mundo aberto do jogo. 
                       Um dos exemplos, o tema principal “Dragonborn” é uma obra criada por um coro composto por 30 pessoas catando em um idioma fantasioso do jogo.
                 </p>
             </article>
            
              <article class="banner">
                 <img class="img-fluid" "max-width: 100%; height: auto;" src="<?= BASE_URL?>/assets/img/bg-20.png">
                 <h1 style="font-weight:bold;">-Street Fighter II</h1>
                 <h2>Compositor: Yoko Shinomura </h2>
                    <p>
                        Lançado em 1991, Street Fighter II foi um jogo muito marcante, não só por mostrar ao mundo o gênero de luta aos jogos, 
                        mas também por sua trilha sonora para cada personagem jogável (estes sendo de diversas etnias diferentes), 
                        que foi criada baseada não na aparência deles, mas sim em sua nacionalidade e cultura, trazendo temas 
                        criados na interpretação característica de cada nação. 
                        Estes temas marcaram tanto, que até nos títulos mais recentes eles ainda são utilizados e recriados em sequências da série.
                 </p>
             </article>  
             
              <article class="banner">
                 <img class="img-fluid" "max-width: 100%; height: auto;" src="<?= BASE_URL?>/assets/img/bg-12.png">
                 <h1 style="font-weight:bold;">--Bastion</h1>
                 <h2>Compositor: Darren Korb</h2>
                    <p>
                      Lançado em 2011, Bastion se baseia em um jogo de exploração isométrica em um mundo criado e definido pelo jogador. 
                      Tendo isso em mente, a trilha fantástica do jogo tem uma forte inspiração na ideia da vida na “Fronteira Americana”, dentro 
                      de um mundo de fantasia, com combinações de batidas fortes em camadas junto com elementos acústicos, com a intenção de 
                      trazer uma combinação diferente de qualquer outra trilha vista. O mais surpreendente desta fantástica obra, é o fato de que esse 
                      alem de ser o primeiro trabalho de seu compositor, Darren Korb, a trilha inteira foi gravada dentro de seu closet em seu próprio apartamento.
                 </p>
             </article>  
                 
                <article class="banner">
                 <img class="img-fluid" "max-width: 100%; height: auto;" src="<?= BASE_URL?>/assets/img/bg-14.png">
                 <h1 style="font-weight:bold;">--Super Mario Bros.</h1>
                 <h2>Compositor: Koji Kondo</h2>
                    <p>
                   Lançado em 1985, a trilha de Super Mario Bros possui um gigantesco peso da indústria de jogos até hoje. 
                   Não só na questão de popularidade, com o clássico tema conhecido por praticamente todos, mas também ele foi o primeiros jogo 
                   que possuía durante seu desenvolvimento, o objetivo de criar uma trilha dinâmica e integral ao jogo, se alterando baseado nas ações 
                   feitas e nos lugares visitados dentro do jogo. A junção de música com jogabilidade com o foco de imersão do jogador, foi um marco de 
                   enorme importância ao cenário de desenvolvimento de jogos, sendo seguido e praticado atualmente.
                 </p>
             </article>   
              
