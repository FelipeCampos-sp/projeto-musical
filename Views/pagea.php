    a alma de todo bom jogo: O som!
</p>

<p> 
   O sound design durante o desenvolvimento de qualquer jogo carrega o importante papel de criar e desenvolver sons capazes de estimular os sentimentos dos seus jogadores, os colocando para dentro do seu universo emocional e fisicamente.

    Há diferentes tipos de efeitos sonoros utilizados para influenciar seus jogadores, como falaremos a seguir.
</p>´

